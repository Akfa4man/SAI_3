        /// <summary>
        /// Возвращает входной вектор длиной 35 для заданной цифры (0..9).
        /// Порядок: построчно, слева направо.
        /// </summary>
        public static double[] GetVector(int digit)
        {
            if (digit < 0 || digit > 9)
                throw new ArgumentOutOfRangeException(nameof(digit));

            var rows = Templates[digit];
            var v = new double[35];
            int idx = 0;

            for (int r = 0; r < 7; r++)
            {
                for (int c = 0; c < 5; c++)
                {
                    v[idx++] = rows[r][c] == '1' ? 1.0 : 0.0;
                }
            }

            return v;
        }
    }
